'use strict';

/* Numbers - Another Example */

// Variable declared and initialized with a number value
let quantity = 5;

// Variable read 
console.log('The quantity is ' + quantity);

// Variable reassigned with a new number value
quantity = 10;

// Variable read again 
console.log('The new quantity is ' + quantity);

// Additional variables declared and initialized with number values
let price = 9.99; // Variable for storing the price of a product
const discount = 0.2; // constant for storing a discount percentage

// Variables read (output to console)
console.log('The price of the product is $' + price);
console.log('The discount is ' + (discount * 100) + '%');

